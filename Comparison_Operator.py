# Operator perbandingan
# Nilai yang dikeluarkan adalah Boolean

print (1 < 2) # Kurang dari
print (1 > 2) # Lebih dar
print (1 <= 2) # Kurang dari sama dengan
print (1 >= 2) # Lebih dari sama dengan
print (1 == 2) # Sama dengan
print (1 != 2) # Tidak sama dengan

# Tidak sama dengan dan sama dengan bisa digunakan untuk String

print ("David" == "David")
print ("David" != "David")